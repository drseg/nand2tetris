import XCTest
@testable import Nand2Tetris

class VMTranslatorTests: XCTestCase {
    var translator: VMTranslator!
    
    override func setUp() {
        translator = VMTranslator()
    }
    
    func translate(_ vmCode: String) -> String {
        translator.translate(vmCode)
    }
    
    func generateConstant(_ c: String) -> String {
        """
        @\(c)
        D=A
        @SP
        A=M
        M=D
        @SP
        M=M+1
        """
    }
    
    func testPushConstant() {
        let vmCode = "push constant 17"
        let assembly = generateConstant("17")
        translate(vmCode) => assembly
    }
    
    func testPushTwoConstants() {
        let vmCode =
                    """
                    push constant 17
                    push constant 22
                    """
        let assembly =
                    """
                    \(generateConstant("17"))
                    \(generateConstant("22"))
                    """
        translate(vmCode) => assembly
    }
    
    func generateArithmetic(_ sign: String) -> String {
        """
        @SP
        A=M
        D=M
        @SP
        M=M-1
        A=M
        D=D\(sign)M
        @SP
        A=M
        M=D
        """
    }
    
    func testAdd() {
        let vmCode = "add"
        let assembly = generateArithmetic("+")
        translate(vmCode) => assembly
    }
    
    func testSub() {
        let vmCode = "sub"
        let assembly = generateArithmetic("-")
        translate(vmCode) => assembly
    }
    
    func testNeg() {
        let vmCode = "neg"
        let assembly =
                    """
                    @SP
                    A=M
                    M=!M
                    @SP
                    A=M
                    M=D
                    """
        translate(vmCode) => assembly
    }
    
    func generateConditional(_ code: String) -> String {
        """
        @SP
        A=M
        D=M
        @SP
        M=M-1
        A=M
        D=D-M
        @SP
        A=M
        M=D
        @\(code)
        D;J\(code)
        D=-1
        (\(code))
        @SP
        A=M
        M=D
        """
    }
    
    func testEQ() {
        let vmCode = "eq"
        let assembly = generateConditional("EQ")
        translate(vmCode) => assembly
    }
    
    func testGT() {
        let vmCode = "gt"
        let assembly = generateConditional("GT")
        translate(vmCode) => assembly
    }
    
    func testLT() {
        let vmCode = "lt"
        let assembly = generateConditional("LT")
        translate(vmCode) => assembly
    }
}

/// Specs:
///
/// Arithmetic commands:
///
/// add
/// sub
/// neg
/// eq ( -> true or false, 0 or -1)
/// gt ( -> true or false, 0 or -1)
/// lt ( -> true or false, 0 or -1)
/// and
/// or
/// not
///
/// Memory access commands:
///
/// (but how do you decide what to initialise the base address to?)
///
/// constant (not on stack, no pop)
///
/// local
/// argument
/// this
/// that
/// pointer
/// temp
/// static
///
/// Ram:
///
/// 0-15 -> Virtual Regsters
/// 16-255 -> Static Vars (auto generated by assembly)
/// 256-2047 -> Stack
/// 2048-16483 -> Heap
/// 16384-24575 -> IO
///
/// Predefined assembly addresses corresponding to VM memory access:
///
/// SP -> Stack Pointer -> RAM[0]
/// LCL -> Local Pointer -> RAM[1]
/// ARG -> Argument Pointer -> RAM[2]
/// THIS -> This pointer -> RAM[3]
/// THAT -> That pointer -> RAM[4]
/// RAM[5-12] -> Temp segment
/// RAM[13-15] -> General purpose registers
